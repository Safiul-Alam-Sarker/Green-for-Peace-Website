function myFunction() {
  var dots = document.getElementById("dots");
  var moreText = document.getElementById("more");
  var btnText = document.getElementById("myBtn");

  if (dots.style.display === "none") {
    dots.style.display = "inline";
    btnText.innerHTML = "Read more";
    moreText.style.display = "none";
  } else {
    dots.style.display = "none";
    btnText.innerHTML = "Read less";
    moreText.style.display = "inline";
  }
}

function DisplayList(items, wrapper, rows_per_page, page) {
  wrapper.innerHTML = "";
  page--;

  let start = rows_per_page * page;
  let end = start + rows_per_page;
  let paginatedItems = items.slice(start, end);

  for (let i = 0; i < paginatedItems.length; i++) {
    let item = paginatedItems[i];
    let item_element = document.createElement("div");
    item_element.classList.add("item");
    item_element.innerHTML = singleBlog(item);
    wrapper.appendChild(item_element);
  }
}

function singleBlog(item) {
  return ` <div class="single-blog">
    <figure><img id="img" src=${item.img} alt=${item.title} sizes="" srcset="">
    </figure>
    <div class="date-time">
        <p><span class="date" id="date">${item.date}</span> <span class="time" id="time">${item.time}</span>
        </p>
    </div>
    <div class="title">
        <h3 id="title">${item.title}
        </h3>
    </div>
    <div class="description">
        <p id="description">${item.description}

        </p>

        <button onclick="myFunction()" id="myBtn">Read more</button>
    </div>
</div>`;
}

function SetupPagination(items, wrapper, rows_per_page) {
  wrapper.innerHTML = "";

  let page_count = Math.ceil(items.length / rows_per_page);
  for (let i = 1; i < page_count + 1; i++) {
    let btn = PaginationButton(i, items);
    wrapper.appendChild(btn);
  }
}

function PaginationButton(page, items) {
  let button = document.createElement("button");
  button.innerText = page;

  if (current_page == page) button.classList.add("active");

  button.addEventListener("click", function () {
    current_page = page;
    DisplayList(items, list_element, rows, current_page);

    let current_btn = document.querySelector(".pagenumbers button.active");
    current_btn.classList.remove("active");

    button.classList.add("active");
  });

  return button;
}

DisplayList(list_items, list_element, rows, current_page);
SetupPagination(list_items, pagination_element, rows);
